    <h1>Getting Started</h1>

    <h3>1. Join Cloudflare Stream Studio</h3>
    <p>In order to start streaming LIVE to your WordPress website, you'll need to create an account with <a href="https://www.cloudflare.com/products/cloudflare-stream/">Cloudflare Stream</a></p>

    <h3>2. Get an API Key</h3>
    <p>An API key allows you to be able to create a stream right from your WordPress admin thru the Cloudflare Stream WP plugin. Try it out! Here is a <a href="https://developers.cloudflare.com/api/get-started/">quick and easy guide</a> to get an API key.</p>

    <h3>3. Create a Stream</h3>
    <p>Once you have obtained an API key, follow the instructions on the <a href="<?php echo site_url()?>/wp-admin/admin.php?page=cloudflare-stream-options">Cloudflare Stream WP options</a> page to get setup your WordPress Cloudflare Stream stream.</p>

    <h3>4. Create an Event</h3>
    <p><a href="<?php echo site_url()?>/wp-admin/edit.php?post_type=cfstream-events">Cloudflare Stream WP Events</a> are like a digital flyer to pass around to your friends on your socials! Add a Cover Image, Date & Time, and name the Event as well as a link to the channel page for your viewers to watch your LIVE stream.</p>
